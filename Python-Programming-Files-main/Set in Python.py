
# Set are used to store multiple element in a single variable.
# Set is a collection which is unordered, unchangable, unindexed.
# It is not allowed duplicate values
#  Note: Set items are unchangeable, but you can remove items and add new items.

fruit_set = {'apple','banana','orange','grapes'}

print(fruit_set)

print(len(fruit_set))

set_info = {'pabitra',21,'soumick',23,'bony','susmita','bony',12,}

print(set_info)

info_set = {'horse','cat','tiger','lion','fox'}
